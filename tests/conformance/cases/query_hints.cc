TEST_P(QueryHintsTest, QueryWithHintsHashJoinBuildSide) {
  if (dialect_ == database_api::DatabaseDialect::POSTGRESQL) {
    // PostgreSQL ignores the join hint instead of failing.

    ZETASQL_EXPECT_OK(Query(
        "/* @join_type=hash_join,hash_join_build_side=build_right */ SELECT "
        "ID, Name, Age FROM Users"));
    ZETASQL_EXPECT_OK(Query(
        "SELECT a.ID, b.Name FROM Users a JOIN /* @"
        "join_method=hash_join,hash_join_build_side=build_right */ Users b "
        "ON false"));
  } else {
    EXPECT_THAT(Query("@{hash_join_build_side=build_left} SELECT ID, Name, Age "
                      "FROM Users"),
                StatusIs(absl::StatusCode::kInvalidArgument));
    ZETASQL_EXPECT_OK(
        Query("@{join_type=hash_join,hash_join_build_side=build_right} SELECT "
              "ID, Name, Age FROM Users"));
    ZETASQL_EXPECT_OK(
        Query("SELECT a.ID, b.Name FROM Users a HASH JOIN "
              "@{hash_join_build_side=build_right} Users b "
              "ON false"));
  }
}

TEST_P(QueryHintsTest, QueryWithGroupHints) {
  if (dialect_ == database_api::DatabaseDialect::POSTGRESQL) {
    ZETASQL_EXPECT_OK(Query(
        "SELECT 1 FROM Users GROUP BY /* @group_method=hash_group */ Name"));
    ZETASQL_EXPECT_OK(Query(
        "SELECT 1 FROM Users GROUP BY /* @group_method=stream_group */ Name"));
    ZETASQL_EXPECT_OK(Query(
        "SELECT 1 FROM Users GROUP BY /* @group_type=hash_group */ Name"));
    ZETASQL_EXPECT_OK(Query(
        "SELECT 1 FROM Users GROUP BY /* @group_type=stream_group */ Name"));
  } else {
    ZETASQL_EXPECT_OK(
        Query("SELECT 1 FROM Users GROUP @{group_method=hash_group} BY Name"));
    ZETASQL_EXPECT_OK(Query(
        "SELECT 1 FROM Users GROUP @{group_method=stream_group} BY Name"));
    ZETASQL_EXPECT_OK(
        Query("SELECT 1 FROM Users GROUP @{group_type=hash_group} BY Name"));
    ZETASQL_EXPECT_OK(
        Query("SELECT 1 FROM Users GROUP @{group_type=stream_group} BY Name"));
  }
}

